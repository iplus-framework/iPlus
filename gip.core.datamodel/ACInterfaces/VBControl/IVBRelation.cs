namespace gip.core.datamodel
{
    /// <summary>
    /// Interface for WPF-Controls that present relations between IACCObjects or IACComponents
    /// </summary>
    [ACClassInfo(Const.PackName_VarioSystem, "en{'IVBReleation'}de{'IVBReleation'}", Global.ACKinds.TACInterface)]
    public interface IVBRelation : IVBContent
    {
        /// <summary>VBConnectorSource is the VBContent to address a SOURCE-IACObject
        /// By setting a ACUrl in XAML, the Control resolves it by calling the IACObject.ACUrlBinding()-Method. 
        /// The ACUrlBinding()-Method returns a Source and a Path which the Control use to create a WPF-Binding to bind the right value and set the WPF-DataContext.
        /// ACUrl's can be either absolute or relative to the DataContext of the parent WPFControl (or the ContextACObject of the parent IACInteractiveObject)</summary>
        /// <value>Relative or absolute ACUrl</value>
        string VBConnectorSource { get; }

        /// <summary>VBConnectorSource is the VBContent to address a TARGET-IACObject
        /// By setting a ACUrl in XAML, the Control resolves it by calling the IACObject.ACUrlBinding()-Method. 
        /// The ACUrlBinding()-Method returns a Source and a Path which the Control use to create a WPF-Binding to bind the right value and set the WPF-DataContext.
        /// ACUrl's can be either absolute or relative to the DataContext of the parent WPFControl (or the ContextACObject of the parent IACInteractiveObject)</summary>
        /// <value>Relative or absolute ACUrl</value>
        string VBConnectorTarget { get; }
    }

}
